import Foundation
import UIKit

extension UIColor
{
    static var themeColor1: UIColor {
        get {
            return UIColor.purple
        }
    }
    static var themeColor2: UIColor {
        get {
            return UIColor.cyan
        }
    }
    static var themeColor3: UIColor {
        get {
            return UIColor.darkGray
        }
    }
    static var error_color: UIColor {
        get {
            return UIColor.red
        }
    }
    static var success_color: UIColor {
        get {
            return UIColor.green
        }
    }
    static var black_color: UIColor {
        get {
            return UIColor.black
        }
    }
    static var white_color: UIColor {
        get {
            return UIColor.white
        }
    }
    static var blue_color: UIColor {
        get {
            return UIColor.blue
        }
    }
    
}

